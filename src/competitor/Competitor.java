package competitor;

public class Competitor {
	protected final String name ;

	/**
	 * create a new Competitor
	 * @param name of the competitor
	 */
	public Competitor(String name) {
		this.name=name;
	
	}
	

	
	
	/**
	 * return the name of the competitor
	 */
	public String toString() {
		return this.name;
	}
	
	
	

}
